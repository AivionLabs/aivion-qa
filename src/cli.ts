// ── validate ─────────────────────────────────────────────────────────────────
cli.command("validate <plan>", "Zod-check a plans/<plan>.yaml without running")
  .action(async (plan: string) => {
    const { getProjectRoot } = await import("./config.js");
    const root = getProjectRoot();
    const planPath = resolvePlanPath(plan, root);
    const ir = loadIrFromDisk(planPath);
    console.log(`[✓] ${planPath} — ${(ir.cases ?? []).length} cases, schema OK`);
  });

// ── run ──────────────────────────────────────────────────────────────────────
cli.command("run [plan]", "Execute a YAML plan")
  .option("--all", "Run all YAML plans in plans/")
  .option("--fail-fast", "Stop after first failure")
  .option("--headed", "Run browser in headed (non-headless) mode")
  .action(async (plan: string | undefined, opts: { all?: boolean; failFast?: boolean; headed?: boolean }) => {
    const { loadConfig, getProjectRoot } = await import("./config.js");
    const { executeIr } = await import("./runner/executor.js");
    const { writeReport, printReportSummary } = await import("./reporter.js");

    const config = loadConfig();
    const root = getProjectRoot();
    const qaToolDir = resolveQaToolDir(root);
    const reportsDir = resolveReportsDir(root);
    mkdirSync(reportsDir, { recursive: true });

    let plans: string[] = [];

    if (opts.all) {
      const plansDir = resolvePlansDir(root);
      plans = readdirSync(plansDir)
        .filter(f => /\.(yaml|yml)$/.test(f) && f !== "base.yaml" && f !== "base.yml")
        .map(f => join(plansDir, f));
    } else if (plan) {
      plans = [resolvePlanPath(plan, root)];
    } else {
      console.error("Specify a plan name or --all");
      process.exit(1);
    }

    let anyFail = false;

    for (const planPath of plans) {
      const planName = planNameFromPath(planPath);
      console.log(`\n${"═".repeat(50)}`);
      console.log(`Running: ${planName}`);
      console.log("═".repeat(50));

      const ir = loadIrFromDisk(planPath);

      const { report, reportDir } = await executeIr({
        ir,
        config,
        reportsDir,
        qaToolDir,
        planName,
        failFast: opts.failFast ?? false,
        headless: !opts.headed,
      });

      const reportPath = await writeReport(report, ir, reportDir, config);
      printReportSummary(report);
      console.log(`Report: ${reportPath}`);

      if (report.failed > 0) anyFail = true;
    }

    process.exit(anyFail ? 1 : 0);
  });

// ── report ────────────────────────────────────────────────────────────────────
cli.command("report [path]", "Open the latest report (or a specific one)").action(async (reportPath?: string) => {
  const { getProjectRoot } = await import("./config.js");
  const { execFile } = await import("node:child_process");

  const root = getProjectRoot();
  const reportsDir = resolveReportsDir(root);

  let target: string;
  if (reportPath) {
    target = resolvePath(reportPath);
  } else {
    const dirs = readdirSync(reportsDir).sort();
    if (dirs.length === 0) {
      console.error("No reports found. Run `qa run <plan>` first.");
      process.exit(1);
    }
    target = join(reportsDir, dirs.at(-1)!, "report.md");
  }

  if (!existsSync(target)) {
    console.error(`Report not found: ${target}`);
    process.exit(1);
  }

  console.log(readFileSync(target, "utf8"));
});

// ── helpers ───────────────────────────────────────────────────────────────────

/** Candidate directories for authored plans, in priority order. */
const PLANS_DIR_CANDIDATES = [
  ".aivion-qa/plans",   // preferred (current default)
  ".aivion-qa",         // flat layout (early migration)
  ".qa-tool/plans",     // legacy (pre-rename)
  ".qa-tool",           // legacy flat
  "plans",              // oldest legacy
] as const;

/**
 * Resolve a plan name to an absolute `.yaml`/`.yml` path. Searches the
 * candidate directories in priority order.
 */
function resolvePlanPath(plan: string, root: string): string {
  if (existsSync(plan) && /\.(yaml|yml)$/.test(plan)) return resolvePath(plan);
  const base = plan.replace(/\.(yaml|yml)$/, "");
  for (const dir of PLANS_DIR_CANDIDATES) {
    for (const ext of ["yaml", "yml"] as const) {
      const p = join(root, dir, `${base}.${ext}`);
      if (existsSync(p)) return p;
    }
  }
  throw new Error(`Plan not found: ${plan}. Searched: ${PLANS_DIR_CANDIDATES.join(", ")}`);
}

/** Base tool dir for cache (schema.json, compiled IR, etc.). */
function resolveQaToolDir(root: string): string {
  for (const dir of [".aivion-qa", ".qa-tool"]) {
    const abs = join(root, dir);
    if (existsSync(abs)) return abs;
  }
  return join(root, ".aivion-qa");
}

/** Reports dir — prefer .aivion-qa/reports/, fall back to legacy locations. */
function resolveReportsDir(root: string): string {
  const candidates = [
    join(root, ".aivion-qa", "reports"),
    join(root, ".qa-tool", "reports"),
    join(root, "reports"),
  ];
  for (const c of candidates) if (existsSync(c)) return c;
  return candidates[0]; // new default — will be created
}

/** Which plans dir is actually in use for this project. */
function resolvePlansDir(root: string): string {
  for (const dir of PLANS_DIR_CANDIDATES) {
    const abs = join(root, dir);
    if (existsSync(abs) && readdirSync(abs).some(f => /\.(yaml|yml)$/.test(f) && f !== "qa.config.yaml")) {
      return abs;
    }
  }
  return join(root, PLANS_DIR_CANDIDATES[0]);
}

function planNameFromPath(path: string): string {
  return basename(path).replace(/\.(yaml|yml)$/, "");
}

/** Load a YAML or JSON IR file from disk, merge with plans/base.yaml if present,
 *  flatten URL groups into cases, and validate against the Zod schema. */
function loadIrFromDisk(path: string): IR {
  const raw = readFileSync(path, "utf8");
  const data = path.endsWith(".json") ? JSON.parse(raw) : parseYaml(raw);

  // Merge with base.yaml if present in the same plans/ directory
  const plansDir = path.replace(/\/[^/]+$/, "");
  for (const baseName of ["base.yaml", "base.yml"]) {
    const basePath = `${plansDir}/${baseName}`;
    if (existsSync(basePath) && basePath !== path) {
      const baseData = parseYaml(readFileSync(basePath, "utf8"));
      mergeBase(data, baseData);
      break;
    }
  }

  // Flatten URL groups into cases. Each group prepends a browser.goto to
  // every case's actions so the executor doesn't need group awareness.
  if (data && typeof data === "object" && Array.isArray((data as Record<string, unknown>).groups)) {
    const groups = (data as { groups: Array<{ url: string; cases: Array<Record<string, unknown>> }> }).groups;
    const flatCases: Array<Record<string, unknown>> = [];
    for (const group of groups) {
      for (const c of group.cases) {
        const existingActions = Array.isArray(c.actions) ? (c.actions as Array<unknown>) : [];
        flatCases.push({
          ...c,
          actions: [{ "browser.goto": group.url }, ...existingActions],
        });
      }
    }
    (data as Record<string, unknown>).cases = [
      ...((data as { cases?: Array<unknown> }).cases ?? []),
      ...flatCases,
    ];
    delete (data as Record<string, unknown>).groups;
  }

  const result = irSchema.safeParse(data);
  if (!result.success) {
    const issues = result.error.issues
      .map(i => `  - ${i.path.join(".") || "<root>"}: ${i.message}`)
      .join("\n");
    throw new Error(`${path} failed schema validation:\n${issues}`);
  }
  return result.data as IR;
}

/** Merge base.yaml values into the plan. Plan's values always win on conflict;
 *  base fills gaps. Arrays (like teardown.sql) are concatenated: base first,
 *  then plan. */
function mergeBase(plan: unknown, base: unknown): void {
  if (!plan || !base || typeof plan !== "object" || typeof base !== "object") return;
  const p = plan as Record<string, unknown>;
  const b = base as Record<string, unknown>;

  for (const key of Object.keys(b)) {
    const bv = b[key];
    const pv = p[key];
    if (pv === undefined) {
      p[key] = bv;
    } else if (Array.isArray(pv) && Array.isArray(bv)) {
      p[key] = [...bv, ...pv];
    } else if (typeof pv === "object" && typeof bv === "object" && !Array.isArray(pv)) {
      mergeBase(pv, bv);
    }
    // Otherwise plan value wins (scalar conflict).
  }
}

