import { cac } from "cac";
import { join, resolve as resolvePath, basename } from "node:path";
import { existsSync, mkdirSync, readdirSync, writeFileSync, readFileSync } from "node:fs";
import { parse as parseYaml } from "yaml";
import type { IR } from "./types.js";
import { irSchema } from "./schemas.js";

const cli = cac("aivion-qa");

// ── init ──────────────────────────────────────────────────────────────────────
cli.command("init", "Scaffold qa.config.yaml, .aivion-qa/ (plans + cache), reports/").action(async () => {
  const cwd = process.cwd();

  if (
    existsSync(join(cwd, ".aivion-qa", "qa.config.yaml")) ||
    existsSync(join(cwd, "qa.config.yaml"))
  ) {
    console.error("qa.config.yaml already exists. Aborting.");
    process.exit(1);
  }

  mkdirSync(join(cwd, ".aivion-qa", "plans"), { recursive: true });
  mkdirSync(join(cwd, ".aivion-qa", "reports"), { recursive: true });

  writeFileSync(join(cwd, ".aivion-qa", "qa.config.yaml"), CONFIG_TEMPLATE);
  writeFileSync(join(cwd, ".env.example"), ENV_TEMPLATE);
  writeFileSync(join(cwd, ".aivion-qa", "plans", "example.yaml"), PLAN_TEMPLATE);

  // Gitignore strategy:
  //   .aivion-qa/*.yaml and base.yaml → TRACKED (authored plans, commit these)
  //   .aivion-qa/schema.json          → IGNORED (regenerated by `qa learn`)
  //   reports/                      → IGNORED (per-run artifacts)
  //   .env                          → IGNORED (secrets)
  const gitignorePath = join(cwd, ".gitignore");
  const gitignoreSnippet = [
    "",
    "# qa-tools",
    ".aivion-qa/schema.json",
    ".aivion-qa/profile.json",
    ".aivion-qa/compiled/",
    ".aivion-qa/reports/",
    ".env",
    "",
  ].join("\n");
  if (existsSync(gitignorePath)) {
    const existing = readFileSync(gitignorePath, "utf8");
    if (!existing.includes(".aivion-qa/schema.json")) {
      writeFileSync(gitignorePath, existing + gitignoreSnippet);
    }
  } else {
    writeFileSync(gitignorePath, gitignoreSnippet.trimStart());
  }

  console.log("Initialized qa-tools project:");
  console.log("  .aivion-qa/qa.config.yaml      — base URLs, auth, DB config");
  console.log("  .aivion-qa/plans/              — put your plan YAMLs here");
  console.log("  .aivion-qa/plans/example.yaml  — starter plan (edit or replace)");
  console.log("  .aivion-qa/reports/            — per-run reports land here (git-ignored)");
  console.log("  .env.example                 — copy to .env and fill in secrets");
  console.log("\nNext: `qa doctor` to verify your stack is reachable.");
});

// ── doctor ───────────────────────────────────────────────────────────────────
cli.command("doctor", "Verify connectivity: URL, DB, Clerk").action(async () => {
  const { loadConfig, getProjectRoot } = await import("./config.js");
  const config = loadConfig();
  const root = getProjectRoot();
  let ok = true;

  console.log("aivion-qa doctor\n");

  // Check URLs
  for (const [name, url] of Object.entries(config.baseUrls)) {
    try {
      const res = await fetch(url, { signal: AbortSignal.timeout(5000) });
      console.log(`  [✓] ${name}: ${url} (${res.status})`);
    } catch (err) {
      console.log(`  [✗] ${name}: ${url} — ${(err as Error).message}`);
      ok = false;
    }
  }

  // Check DB
  try {
    const { Pool } = await import("pg");
    const dbUrl = process.env[config.db.connectionStringEnv];
    if (!dbUrl) throw new Error(`${config.db.connectionStringEnv} not set`);

    // `pg` silently strips non-standard URL dialects like `postgresql+asyncpg://`.
    // Warn so users don't ship a SQLAlchemy-style URL that only works by accident.
    const scheme = dbUrl.split("://")[0] ?? "";
    if (scheme !== "postgresql" && scheme !== "postgres") {
      console.log(`  [!] database: URL scheme "${scheme}://" is non-standard for Node pg — expected "postgresql://". pg will tolerate it, but consider normalizing.`);
    }

    const pool = new Pool({ connectionString: dbUrl });
    await pool.query("SELECT 1");
    await pool.end();
    console.log(`  [✓] database: connected (SELECT 1)`);
  } catch (err) {
    console.log(`  [✗] database: ${(err as Error).message}`);
    ok = false;
  }

  // Check Clerk
  try {
    const key = process.env[config.auth.secretKeyEnv];
    if (!key) throw new Error(`${config.auth.secretKeyEnv} not set`);
    const res = await fetch("https://api.clerk.com/v1/users?limit=1", {
      headers: { Authorization: `Bearer ${key}` },
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    console.log(`  [✓] clerk: API key valid`);
  } catch (err) {
    console.log(`  [✗] clerk: ${(err as Error).message}`);
    ok = false;
  }

  // Check Playwright browsers
  try {
    const { chromium } = await import("playwright");
    const execPath = chromium.executablePath();
    if (!existsSync(execPath)) {
      console.log(`  [✗] playwright chromium: not installed`);
      console.log(`       Fix: aivion-qa install-browsers  (or: npx playwright install chromium)`);
      ok = false;
    } else {
      console.log(`  [✓] playwright chromium: installed`);
    }
  } catch (err) {
    console.log(`  [✗] playwright chromium: ${(err as Error).message}`);
    ok = false;
  }

  // Check AI
  if (config.ai.mode === "claude_cli") {
    const { execFile } = await import("node:child_process");
    const { promisify } = await import("node:util");
    const execFileAsync = promisify(execFile);
    try {
      await execFileAsync("claude", ["--version"], { timeout: 5000 });
      console.log(`  [✓] claude CLI: found`);
    } catch {
      console.log(`  [!] claude CLI: not found — install Claude Code or switch to sdk mode`);
    }
  }

  console.log(ok ? "\nAll checks passed." : "\nSome checks failed. Fix the issues above before running plans.");
  process.exit(ok ? 0 : 1);
});

